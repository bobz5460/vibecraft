impl FontTexture {
    pub fn new(device: &Device, queue: &Queue) -> Self {
        let font_width = 128u32;
        let font_height = 48u32;
        let mut pixels = vec![0u8; (font_width * font_height * 4) as usize];

        for char_idx in 0..96u32 {
            let col = char_idx % 16;
            let row = char_idx / 16;
            for py in 0..8u32 {
                let byte = FONT_DATA[(char_idx * 8 + py) as usize];
                for px in 0..8u32 {
                    let bit = (byte >> (7 - px)) & 1;
                    let tx = col * 8 + px;
                    let ty = row * 8 + py;
                    let pi = ((ty * font_width + tx) * 4) as usize;
                    if bit == 1 {
                        pixels[pi] = 255;
                        pixels[pi + 1] = 255;
                        pixels[pi + 2] = 255;
                        pixels[pi + 3] = 255;
                    } else {
                        pixels[pi] = 0;
                        pixels[pi + 1] = 0;
                        pixels[pi + 2] = 0;
                        pixels[pi + 3] = 0;
                    }
                }
            }
        }

        let texture_size = Extent3d {
            width: font_width,
            height: font_height,
            depth_or_array_layers: 1,
        };

        let texture = device.create_texture(&TextureDescriptor {
            label: Some("font"),
            size: texture_size,
            mip_level_count: 1,
            sample_count: 1,
            dimension: TextureDimension::D2,
            format: TextureFormat::Rgba8UnormSrgb,
            usage: TextureUsages::TEXTURE_BINDING | TextureUsages::COPY_DST,
            view_formats: &[],
        });

        queue.write_texture(
            TexelCopyTextureInfo {
                texture: &texture,
                mip_level: 0,
                origin: Origin3d::ZERO,
                aspect: TextureAspect::All,
            },
            &pixels,
            TexelCopyBufferLayout {
                offset: 0,
                bytes_per_row: Some(font_width * 4),
                rows_per_image: Some(font_height),
            },
            texture_size,
        );

        let view = texture.create_view(&TextureViewDescriptor::default());
        let sampler = device.create_sampler(&SamplerDescriptor {
            label: Some("font_sampler"),
            address_mode_u: AddressMode::ClampToEdge,
            address_mode_v: AddressMode::ClampToEdge,
            address_mode_w: AddressMode::ClampToEdge,
            mag_filter: FilterMode::Nearest,
            min_filter: FilterMode::Nearest,
            mipmap_filter: FilterMode::Nearest,
            ..Default::default()
        });

        FontTexture { texture, view, sampler }
    }

    /// Build a dark semi-transparent background quad
    pub fn build_text_bg(&self, x: f32, y: f32, w: f32, h: f32) -> (Vec<TextVertex>, Vec<u32>) {
        let verts = vec![
            TextVertex { pos: [x, y], uv: [0.0, 0.0] },
            TextVertex { pos: [x + w, y], uv: [0.0, 0.0] },
            TextVertex { pos: [x + w, y + h], uv: [0.0, 0.0] },
            TextVertex { pos: [x, y + h], uv: [0.0, 0.0] },
        ];
        let indices = vec![0, 1, 2, 0, 2, 3];
        (verts, indices)
    }

    /// Build text vertex data for a string at screen position (x, y)
    /// Character size (char_w × char_h) in screen pixels.
    pub fn build_text(&self, text: &str, x: f32, y: f32, char_w: f32, char_h: f32) -> (Vec<TextVertex>, Vec<u32>) {
        let fw = 128.0;
        let fh = 48.0;

        let mut verts = Vec::with_capacity(text.len() * 4);
        let mut indices = Vec::with_capacity(text.len() * 6);

        let mut cursor_x = x;
        for ch in text.chars() {
            let code = ch as u32;
            if code < 32 || code > 127 { continue; }
            let char_idx = code - 32;
            let tc = (char_idx % 16) as f32;
            let tr = (char_idx / 16) as f32;

            let u0 = (tc * 8.0) / fw;
            let v0 = (tr * 8.0) / fh;
            let u1 = ((tc + 1.0) * 8.0) / fw;
            let v1 = ((tr + 1.0) * 8.0) / fh;

            let base = verts.len() as u32;
            verts.push(TextVertex { pos: [cursor_x, y], uv: [u0, v0] });
            verts.push(TextVertex { pos: [cursor_x + char_w, y], uv: [u1, v0] });
            verts.push(TextVertex { pos: [cursor_x + char_w, y + char_h], uv: [u1, v1] });
            verts.push(TextVertex { pos: [cursor_x, y + char_h], uv: [u0, v1] });
            indices.extend_from_slice(&[base, base + 1, base + 2, base, base + 2, base + 3]);

            cursor_x += char_w;
        }

        (verts, indices)
    }
}
